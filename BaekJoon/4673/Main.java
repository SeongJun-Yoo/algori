import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);

        String str = scan.nextLine();
     
        System.out.println(aski(str.charAt(0)));
        
    }

    static int aski(char ch) {
        int num = ch ;
       
        return num;
    }
}